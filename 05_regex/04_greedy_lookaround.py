r"""
正则第四层：贪婪 vs 非贪婪 + 前后断言
─────────────────────────────────────────
内容：
1. 贪婪：默认行为，吃得越多越好
2. 非贪婪：?? *? +? {n,m}?  能少则少
3. 占有量词：*+ ++ ?+  (Python 3.11+) 防回溯爆炸
4. 前后断言（lookaround）：(?=...) (?!...) (?<=...) (?<!...)
5. 实战：HTML 标签提取、密码强度校验、千分位插逗号
"""

import re


# ══════════════════════════════════════════════════════
# 1. 贪婪匹配 ── 默认行为
# ══════════════════════════════════════════════════════
# *、+、?、{n,m} 默认都是贪婪：能多吃就多吃

text = "<b>粗体</b><i>斜体</i>"

# 想抓单个标签内容，结果一锅端
print(re.findall(r"<.+>", text))
# ['<b>粗体</b><i>斜体</i>']
#
# 解释：. 匹配任意字符，+ 至少 1 次（贪婪）
#       从第一个 < 开始，引擎尽量多吃，
#       直到最后一个 > 才停下来


# ══════════════════════════════════════════════════════
# 2. 非贪婪匹配 ── 加 ? 让量词"能少则少"
# ══════════════════════════════════════════════════════

# 在量词后加 ? : *? +? ?? {n,m}?
print(re.findall(r"<.+?>", text))
# ['<b>', '</b>', '<i>', '</i>']  ← 每个标签独立


# 量词对照表
#   *   贪婪：0 或多次，尽量多
#   *?  非贪婪：0 或多次，尽量少
#   +   贪婪：1 或多次，尽量多
#   +?  非贪婪：1 或多次，尽量少
#   ?   贪婪：0 或 1 次（有就吃）
#   ??  非贪婪：0 或 1 次（能不吃就不吃）
#   {2,5}  贪婪：尽量靠近 5
#   {2,5}? 非贪婪：尽量靠近 2


# ══════════════════════════════════════════════════════
# 3. 更好的替代：用"否定字符类"代替非贪婪
# ══════════════════════════════════════════════════════
# .+? 可读但慢；[^x]+ 又快又精确

# 抓标签内容
print(re.findall(r"<[^>]+>", text))
# ['<b>', '</b>', '<i>', '</i>']
#
# [^>]+ 含义：连续匹配"非 >"的字符，直到遇到 >
# 比 .+? 更快（不需要回溯）也更明确


# 提取双引号里的内容
text = 'name="alice" age="30"'
print(re.findall(r'"([^"]+)"', text))    # ['alice', '30']
print(re.findall(r'"(.+?)"',   text))    # ['alice', '30']  ← 也能用，但慢


# ══════════════════════════════════════════════════════
# 4. 占有量词（possessive） ── Python 3.11+
# ══════════════════════════════════════════════════════
# 写法：*+  ++  ?+  {n,m}+
# 含义：贪婪 + 不允许回溯
# 用途：防止"灾难性回溯"导致正则卡死

# 普通贪婪 + 没法匹配的尾部 → 大量回溯
# import time
# pat = re.compile(r"(a+)+b")  # 嵌套量词，遇到 "aaaa...c" 会指数爆炸

# 占有量词版（Python 3.11+）：
# pat = re.compile(r"(a++)+b")  # 不回溯，要么直接成功要么直接失败

# 实战中：能用否定字符类就用否定字符类，是最普适的解法


# ══════════════════════════════════════════════════════
# 5. 前瞻断言 (?=...) ── "后面必须是 ..."
# ══════════════════════════════════════════════════════
# 断言只检查"是不是"，不消费字符（位置不前进）

# 找"后面跟着 元 的数字"，但只要数字本身
text = "苹果 5 元，香蕉 3 块，葡萄 15 元"
print(re.findall(r"\d+(?=\s*元)", text))
# ['5', '15']    ← '3' 后面跟的是"块"，不算


# ══════════════════════════════════════════════════════
# 6. 否定前瞻 (?!...) ── "后面不能是 ..."
# ══════════════════════════════════════════════════════

# 找"后面不是 .com 的网址前缀"
text = "site1.com site2.cn site3.com site4.org"
print(re.findall(r"\bsite\d+(?!\.com)", text))
# ['site2', 'site4']    ← 后面接的不是 .com 的


# 经典用例：密码校验
def is_strong_password(p: str) -> bool:
    """
    至少 8 位
    必须含大写、小写、数字
    """
    return bool(re.match(
        r"^(?=.*[A-Z])(?=.*[a-z])(?=.*\d).{8,}$",
        p
    ))

# 解释：
#   ^                  开头
#   (?=.*[A-Z])        前瞻：后面某处必须有大写字母
#   (?=.*[a-z])        前瞻：后面某处必须有小写字母
#   (?=.*\d)           前瞻：后面某处必须有数字
#   .{8,}              至少 8 位任意字符
#   $                  结尾
#
# 多个前瞻同时生效，这是"用断言做条件叠加"的标准手法

print(is_strong_password("Abc12345"))   # True
print(is_strong_password("abc12345"))   # False  缺大写
print(is_strong_password("Abcdefgh"))   # False  缺数字
print(is_strong_password("Ab12"))       # False  太短


# ══════════════════════════════════════════════════════
# 7. 后顾断言 (?<=...) ── "前面必须是 ..."
# ══════════════════════════════════════════════════════
# 注意：Python 的后顾断言要求"长度固定"

# 提取价格的数字部分（不要￥符号）
text = "¥99.5 ¥1280 ¥0.5"
print(re.findall(r"(?<=¥)\d+(?:\.\d+)?", text))
# ['99.5', '1280', '0.5']


# ══════════════════════════════════════════════════════
# 8. 否定后顾 (?<!...) ── "前面不能是 ..."
# ══════════════════════════════════════════════════════

# 找"不在 # 后面的数字"
text = "id=123 #456 size=789 #0"
print(re.findall(r"(?<!#)\d+", text))
# ['123', '23', '789', '0']
#
# 注意结果有 '23'：因为 #456 中 '4' 前面是 #，被排除；
# 但 '23' 这个位置前面是 '4'，不是 #，所以匹配成功
# 这就是"按位置检查"的特性


# 改进：要"独立的数字"，加上 \b
print(re.findall(r"(?<!#)\b\d+", text))
# ['123', '789', '0']        # 0 是因为 #0 中 0 前面是 #，但 \b 要求边界...

# 更稳的写法：组合多个断言
print(re.findall(r"(?<![#\w])\d+", text))
# ['123', '789']   ← 前面既不是 # 也不是字母数字


# ══════════════════════════════════════════════════════
# 9. 四种断言对照速查
# ══════════════════════════════════════════════════════
#   (?=X)     positive lookahead   后面是 X
#   (?!X)     negative lookahead   后面不是 X
#   (?<=X)    positive lookbehind  前面是 X
#   (?<!X)    negative lookbehind  前面不是 X
#
# 关键性质：
#   断言不消费字符，只检查"位置周围"的情况
#   匹配指针不会前进
#   常用于"想匹配 X，但 X 必须在某种上下文中"


# ══════════════════════════════════════════════════════
# 10. 实战：千分位插逗号
# ══════════════════════════════════════════════════════
# 把 1234567890 变成 1,234,567,890

def add_commas(num_str: str) -> str:
    return re.sub(r"(?<=\d)(?=(\d{3})+$)", ",", num_str)


# 解释：
#   (?<=\d)         前面必须是数字（保证不在最前面插）
#   (?=(\d{3})+$)   后面必须是 N×3 个数字直到结尾
# 因为是断言，匹配的"位置"宽度为 0，sub 在这些位置插入 ","

print(add_commas("1234567890"))       # 1,234,567,890
print(add_commas("1000"))             # 1,000
print(add_commas("99"))               # 99    ← 不到 4 位不插


# ══════════════════════════════════════════════════════
# 11. 实战：抓 HTML 链接但不抓配对的闭合标签
# ══════════════════════════════════════════════════════

html = '<a href="https://a.com">链接1</a> <a href="https://b.com">链接2</a>'

# 提取 href，要求前面紧跟 a 标签
links = re.findall(r'(?<=<a href=")[^"]+', html)
print(links)
# ['https://a.com', 'https://b.com']


# ══════════════════════════════════════════════════════
# 12. 工程小结
# ══════════════════════════════════════════════════════
#
# 贪婪 vs 非贪婪 vs 否定字符类：
#   能用 [^x]+ 就用 [^x]+    （快、准、不回溯）
#   想要"两个边界字符之间的内容" → 否定字符类
#   只是少量文本 + 简单场景 → .+? 也能用
#
# 断言的典型用法：
#   - 条件叠加（密码校验：多个 (?=...) 串联）
#   - 按上下文过滤（如 (?<=¥) ）
#   - 零宽位置插入（如千分位逗号）
#
# 别滥用断言：
#   能用普通分组解决的问题，不要硬上断言
#   断言会让正则更难读，性能也未必更好
