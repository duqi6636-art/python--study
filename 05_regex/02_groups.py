r"""
正则第二层：分组与提取
─────────────────────────────────────────
内容：
1. 普通分组 (...) ── 把匹配的一部分"取出来"
2. 命名分组 (?P<name>...) ── 用名字访问，更可读
3. 非捕获分组 (?:...) ── 只分组、不捕获
4. 反向引用 \1 \2 ── 引用前面分组匹配到的内容
5. 实战：解析日志、URL、版本号
"""

import re


# ══════════════════════════════════════════════════════
# 1. 普通分组 ── 用 () 包起来就是一个分组
# ══════════════════════════════════════════════════════

# 没有分组：只能拿到整个匹配
m = re.search(r"\d{4}-\d{2}-\d{2}", "今天是 2026-05-25")
print(m.group())        # '2026-05-25'  整体

# 有分组：可以分别拿到年/月/日
m = re.search(r"(\d{4})-(\d{2})-(\d{2})", "今天是 2026-05-25")
print(m.group())        # '2026-05-25'  整个匹配
print(m.group(1))       # '2026'        第 1 个分组
print(m.group(2))       # '05'
print(m.group(3))       # '25'
print(m.groups())       # ('2026', '05', '25')  所有分组


# ══════════════════════════════════════════════════════
# 2. findall 配合分组的行为
# ══════════════════════════════════════════════════════
#
# 没分组 → 返回字符串列表
# 1 个分组 → 返回该分组的字符串列表
# 多个分组 → 返回元组的列表

text = "a:1 b:2 c:3"
print(re.findall(r"\w:\d", text))      # ['a:1', 'b:2', 'c:3']  无分组
print(re.findall(r"(\w):\d", text))    # ['a', 'b', 'c']        1 个分组
print(re.findall(r"(\w):(\d)", text))  # [('a','1'), ('b','2'), ('c','3')]


# ══════════════════════════════════════════════════════
# 3. 命名分组 ── 推荐！可读性大增
# ══════════════════════════════════════════════════════
# 语法：(?P<名字>...)
# 取值：m.group("名字") 或 m["名字"]

m = re.search(
    r"(?P<year>\d{4})-(?P<month>\d{2})-(?P<day>\d{2})",
    "今天是 2026-05-25"
)
print(m.group("year"))     # '2026'
print(m.group("month"))    # '05'
print(m["day"])            # '25'   ← 也可以用下标语法

# groupdict() : 直接拿到 dict，超好用
print(m.groupdict())       # {'year': '2026', 'month': '05', 'day': '25'}


# ══════════════════════════════════════════════════════
# 4. 非捕获分组 (?:...) ── 只分组、不占编号
# ══════════════════════════════════════════════════════
#
# 场景：你想用 () 把一段子表达式"组合起来"
# （比如配合 |、+ 等量词），但又不想把它捕获成结果

# 想匹配 "Mr. / Mrs. / Ms."
# 不用非捕获的写法：
m = re.findall(r"(Mr|Mrs|Ms)\. (\w+)", "Mr. Smith and Mrs. Lee")
print(m)    # [('Mr', 'Smith'), ('Mrs', 'Lee')]
# 多了一个 ('Mr', ...) 我可能并不需要

# 用非捕获分组：
m = re.findall(r"(?:Mr|Mrs|Ms)\. (\w+)", "Mr. Smith and Mrs. Lee")
print(m)    # ['Smith', 'Lee']    干净


# ══════════════════════════════════════════════════════
# 5. 反向引用 \1 \2 ── 引用前面分组的"实际匹配内容"
# ══════════════════════════════════════════════════════
#
# 场景：要匹配"重复"模式

# 找重复的单词
text = "the the cat sat on the the mat"
matches = re.findall(r"\b(\w+) \1\b", text)
#                          ↑     ↑
#                       第1组   引用第1组实际匹配的内容
print(matches)   # ['the', 'the']

# 命名分组的反向引用：(?P=name)
matches = re.findall(r"\b(?P<w>\w+) (?P=w)\b", text)
print(matches)   # ['the', 'the']

# 匹配 HTML/XML 配对标签（简化版）
text = "<b>粗</b> <i>斜</i> <b>错配</i>"
m = re.findall(r"<(\w+)>(.*?)</\1>", text)
#                  ↑           ↑
#                第1组      回引第1组（必须是同一个标签名）
print(m)   # [('b', '粗'), ('i', '斜')]   ← <b>错</i> 不会被匹配


# ══════════════════════════════════════════════════════
# 6. 实战：解析日志
# ══════════════════════════════════════════════════════

log = "[2026-05-25 10:30:45] INFO  user=alice action=login ip=192.168.1.10"

pattern = re.compile(
    r"\["
    r"(?P<time>\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2})"
    r"\]\s+"
    r"(?P<level>\w+)\s+"
    r"user=(?P<user>\w+)\s+"
    r"action=(?P<action>\w+)\s+"
    r"ip=(?P<ip>\S+)"
)
m = pattern.search(log)
print(m.groupdict())
# {'time': '2026-05-25 10:30:45', 'level': 'INFO',
#  'user': 'alice', 'action': 'login', 'ip': '192.168.1.10'}


# ══════════════════════════════════════════════════════
# 7. 实战：拆 URL
# ══════════════════════════════════════════════════════

url = "https://api.example.com:8080/v1/users?id=42&name=alice"

pattern = re.compile(
    r"(?P<scheme>https?)://"           # https 或 http
    r"(?P<host>[\w.-]+)"               # 域名
    r"(?::(?P<port>\d+))?"             # 可选端口（非捕获 :，但端口本身要捕获）
    r"(?P<path>/[\w/]*)"               # 路径
    r"(?:\?(?P<query>.*))?"            # 可选 query
)
m = pattern.search(url)
print(m.groupdict())


# ══════════════════════════════════════════════════════
# 8. 实战：解析版本号
# ══════════════════════════════════════════════════════

versions = ["v1.2.3", "v2.0", "1.5.10-beta", "release-3.4.1"]

pattern = re.compile(r"(?P<major>\d+)\.(?P<minor>\d+)(?:\.(?P<patch>\d+))?")
for v in versions:
    m = pattern.search(v)
    if m:
        d = m.groupdict()
        print(f"  {v}  →  major={d['major']} minor={d['minor']} patch={d['patch']}")


# ══════════════════════════════════════════════════════
# 9. 关键速查
# ══════════════════════════════════════════════════════
#
# (...)         捕获分组       group(1) / groups()
# (?P<x>...)    命名分组       group("x") / groupdict()
# (?:...)       非捕获         不计入分组编号
# \1, \2        反向引用       同一匹配中复用前面的捕获
# (?P=x)        命名反向引用   同上但用名字
#
# 实战建议：
#   - 字段多 → 一律用命名分组，不要数 \1 \2
#   - 只是想配合量词 → 用 (?:...) 非捕获分组
#   - 解析结构化数据 → groupdict() 直接出 dict，舒服
